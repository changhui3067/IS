package com.successfactors.sef.service;

import java.util.Map;

import com.successfactors.sca.ServiceCommand;

/**
 * SetEntityEventsMap
 * 
 */

public class SetEntityEventsMap implements ServiceCommand<String> {

  private String entityName;
  private String eventType;
  private String effectiveDate;
  private Map<String, String> entityKeyValues;

  /**
   * @return the entityName
   */
  public String getEntityName() {
    return entityName;
  }

  /**
   * @param entityName
   *          the entityName to set
   */
  public void setEntityName(final String entityName) {
    this.entityName = entityName;
  }

  /**
   * @return the eventType
   */
  public String getEventType() {
    return eventType;
  }

  /**
   * @param eventType
   *          the eventType to set
   */
  public void setEventType(final String eventType) {
    this.eventType = eventType;
  }

  /**
   * @return the entityKeyValues
   */
  public Map<String, String> getEntityKeyValues() {
    return entityKeyValues;
  }

  /**
   * @param entityKeyValues
   *          the entityKeyValues to set
   */
  public void setEntityKeyValues(final Map<String, String> entityKeyValues) {
    this.entityKeyValues = entityKeyValues;
  }

  /**
   * @return the effectiveDate
   */
  public String getEffectiveDate() {
    return effectiveDate;
  }

  /**
   * @param effectiveDate the effectiveDate to set
   */
  public void setEffectiveDate(String effectiveDate) {
    this.effectiveDate = effectiveDate;
  }

}
